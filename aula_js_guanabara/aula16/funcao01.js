




function parimp(n) {
    // parametro n
    if (n % 2 == 0) {
        return 'Par!'
        // return avisa que será retornado ao parametro o determinado valor a seguinte
    } else {
        return 'Impar!'
    }
}

console.log(parimp(3))
// ao escrever uma função com um valor dentro do parênteses você está dando um valor para um parâmetro é como se estivesse escrito n = 3






